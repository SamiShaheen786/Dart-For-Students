/*
============================================================
             WHAT IS PROGRAMMING?
============================================================

Programming is the process of writing instructions for a
computer to follow in order to perform a specific task.
These instructions are written in a programming language.

----------------------------------------------------------
REAL LIFE ANALOGY:
----------------------------------------------------------
Think of a recipe for making tea:
  Step 1 → Boil water
  Step 2 → Add tea leaves
  Step 3 → Add milk and sugar
  Step 4 → Serve hot

A program works the same way.
You give the computer step-by-step instructions,
and it follows them exactly as written.

----------------------------------------------------------
WHAT CAN PROGRAMMING DO?
----------------------------------------------------------
✔ Build mobile apps (WhatsApp, Instagram, YouTube)
✔ Build websites (Google, Amazon, GitHub)
✔ Create games (PUBG, Minecraft)
✔ Power AI and machine learning systems
✔ Automate repetitive tasks (like sending 1000 emails)
✔ Analyze large amounts of data

----------------------------------------------------------
WHAT DOES A PROGRAM CONSIST OF?
----------------------------------------------------------
Every program — no matter the language — is built from
these core building blocks:

  Variables      → store data (name, age, score)
  Data Types     → type of data (text, number, true/false)
  Operators      → perform actions (+, -, *, /)
  Control Flow   → make decisions (if/else, switch)
  Loops          → repeat tasks (for, while)
  Functions      → reusable blocks of code
  Classes        → blueprint for real-world objects (OOP)

----------------------------------------------------------
KEY SKILLS A PROGRAMMER NEEDS:
----------------------------------------------------------
1. Logical thinking    → break big problems into small steps
2. Attention to detail → one wrong character breaks the code
3. Problem solving     → find the best way to do something
4. Patience            → debugging takes time — that is normal

----------------------------------------------------------
PROGRAMMING LANGUAGES (examples):
----------------------------------------------------------
Dart       → Mobile & web apps (Flutter)
Python     → AI, data science, automation
Java       → Android, enterprise applications
JavaScript → Web development (frontend)
C++        → Games, system-level programming

----------------------------------------------------------
IMPORTANT NOTE:
----------------------------------------------------------
Every programming language has different syntax (rules),
but the core concepts — variables, loops, functions, OOP —
are the SAME across all languages.

Once you truly understand one language, picking up the
next one becomes much faster.
*/

