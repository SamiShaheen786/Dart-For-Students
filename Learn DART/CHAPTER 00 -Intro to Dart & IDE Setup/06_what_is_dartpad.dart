/*
============================================================
                 WHAT IS DARTPAD?
============================================================

DartPad is an online Dart editor that runs in your browser.
No installation. No setup. Just open and start coding.

URL: https://dartpad.dev

----------------------------------------------------------
WHY DARTPAD EXISTS:
----------------------------------------------------------
Setting up a full development environment takes time.
For absolute beginners, that can be a barrier.

DartPad solves this by letting you:
  → Write Dart code directly in the browser
  → Run it instantly and see the output
  → Experiment without fear of breaking anything
  → Share your code with others via a link

----------------------------------------------------------
WHAT DARTPAD LOOKS LIKE:
----------------------------------------------------------
Left panel  → Code editor (where you write Dart code)
Right panel → Output (what your code prints or shows)
Run button  → Executes your code instantly

It also supports Flutter — you can see a live UI preview
of Flutter widgets right inside DartPad.

----------------------------------------------------------
FEATURES OF DARTPAD:
----------------------------------------------------------
✔ Syntax highlighting      → code is color-coded for readability
✔ Auto-complete            → suggests code as you type
✔ Error highlighting       → shows mistakes before you run
✔ Flutter support          → run UI code and see live preview
✔ Share via link           → send your code to anyone
✔ Works on any device      → phone, tablet, laptop — no install needed

----------------------------------------------------------
WHEN TO USE DARTPAD:
----------------------------------------------------------
✔ When you are just starting and haven't installed Dart yet
✔ When you want to quickly test a small piece of code
✔ When you want to share code with a classmate or teacher
✔ When you are away from your computer and need to practice

✗ Not suitable for large projects or full app development
  (for that, use VS Code + Dart SDK — covered in file 10)

----------------------------------------------------------
HOW TO USE DARTPAD — STEP BY STEP:
----------------------------------------------------------
Step 1 → Open browser and go to: https://dartpad.dev
Step 2 → You will see a default "Hello World" program
Step 3 → Clear the editor and write your own code
Step 4 → Click the green "Run" button
Step 5 → See the output on the right side
Step 6 → Modify the code and run again — experiment freely!

----------------------------------------------------------
DARTPAD vs LOCAL SETUP:
----------------------------------------------------------
Feature           | DartPad        | Local (VS Code + SDK)
------------------|----------------|----------------------
Installation      | Not needed     | Required
Internet needed   | Yes            | No (after setup)
Full project      | No             | Yes
Flutter preview   | Basic          | Full
Best for          | Beginners      | Full development
Speed             | Instant start  | More setup time

----------------------------------------------------------
RECOMMENDED FOR BEGINNERS:
----------------------------------------------------------
Start with DartPad for the first few chapters.
Once you are comfortable with Dart basics,
move to the full local setup (VS Code + Dart SDK).
*/

