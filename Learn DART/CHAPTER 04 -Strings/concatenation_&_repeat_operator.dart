void main() {
  // + concatenation operator
  String firstname = "Sami";
  String lastname = "Ullah";
  String fulname = firstname + lastname;
  print(fulname);
  print(firstname + lastname);
  print("Sami" + "Ullah");

  // * repeat Operator
  String text = "hi" * 3;
  print(text);
  print("Do" * 5);
}
